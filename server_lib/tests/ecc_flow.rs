use server_utilities::ecc::{ecc_decrypt, ecc_encrypt};
use napi::bindgen_prelude::Buffer;

#[test]
fn test_ecc_flow() {
    let sk: Vec<u8> = vec![
        0xe5, 0xa8, 0x60, 0xf0, 0x2a, 0xa1, 0x67, 0x08, 0x21, 0x51, 0xa1, 0x7b, 0xc6, 0x9a, 0x1e,
        0x7a, 0xb1, 0xb4, 0x1d, 0x5a, 0x8e, 0xc9, 0x16, 0xca, 0x09, 0xc0, 0x88, 0xe4, 0x81, 0x00,
        0x4d, 0x88
    ];
    let pk: Vec<u8> = vec![
        0x04, 0x89, 0xf4, 0x6d, 0x82, 0x8e, 0x9d, 0x5f, 0xf5, 0x10, 0x00, 0x72, 0x33, 0xa7, 0x25,
        0x16, 0x23, 0x7d, 0x44, 0xdc, 0x9a, 0x90, 0x16, 0x02, 0xe9, 0x5f, 0x6b, 0x0d, 0xe0, 0x84,
        0xd5, 0xde, 0x30, 0x01, 0x5b, 0x45, 0x87, 0x73, 0x60, 0x9d, 0x9a, 0xaa, 0xf2, 0xdf, 0xad,
        0x4b, 0x33, 0xdc, 0x70, 0x3e, 0x93, 0x2e, 0x6c, 0x05, 0x80, 0xc4, 0x99, 0x8b, 0xed, 0x11,
        0x2a, 0xd1, 0xd6, 0xcb, 0xeb
    ];

    let msg = b"hello world".to_vec();

    let encrypted = ecc_encrypt(Buffer::from(pk), Buffer::from(msg.clone())).expect("encryption failed");
    let decrypted = ecc_decrypt(Buffer::from(sk), encrypted).expect("decryption failed");

    assert_eq!(msg, decrypted.to_vec());
}
